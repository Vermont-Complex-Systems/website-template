<script>
    let {
        xScale,
        innerWidth,
        innerHeight,
        ticks,
        label,
    } = $props();
</script>

<!-- Grid lines -->
{#each ticks as tick}
    <line
        x1={xScale(tick)}
        x2={xScale(tick)}
        y1={0}
        y2={innerHeight}
        stroke="#e0e0e0"
        stroke-width="1"
    />
{/each}

<!-- Axis line -->
<line x1={0} x2={innerWidth} y1={innerHeight} y2={innerHeight} stroke="#333" stroke-width="1" />

<!-- Tick labels -->
{#each ticks as tick}
    <text
        x={xScale(tick)}
        y={innerHeight + 20}
        text-anchor="middle"
        font-size="12"
    >
        {tick}
    </text>
{/each}

<!-- Axis label -->
<text
    x={innerWidth / 2}
    y={innerHeight + 50}
    text-anchor="middle"
    font-size="14"
    font-weight="500"
>
    {label}
</text>
